function telephoneCheck(str) {
  const formats = /^\d{3,3}-\d{3,3}-\d{4,4}$|^\(\d{3,3}\)\d{3,3}-\d{4,4}$|^\(\d{3,3}\)\s{1,1}\d{3,3}-\d{4,4}$|^\d{3,3}\s{1,1}\d{3,3}\s{1,1}\d{4,4}$|^\d{10,10}$|^\d{1,1}\s{1,1}\d{3,3}\s{1,1}\d{3,3}\s{1,1}\d{4,4}$|^\d{1,1}\s{1,1}\d{3,3}-\d{3,3}-\d{4,4}$|^[1]\s{1,1}\(\d{3,3}\)\s{1,1}\d{3,3}-\d{4,4}$|^[1]\(\d{3,3}\)\d{3,3}-\d{4,4}$/

  return formats.test(str);
}

console.log(telephoneCheck("1(555)555-5555"));

